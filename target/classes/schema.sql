DROP TABLE IF EXISTS `USERS`;
CREATE TABLE `USERS` (
    EMAIL varchar(255) NOT NULL,
    PASSWORD varchar(255) NOT NULL
);

DROP TABLE IF EXISTS `TEXTURES`;
CREATE TABLE `TEXTURES` (
    EMAIL varchar(255) NOT NULL,
    TEXTURE varchar(1024) NOT NULL
);

DROP TABLE IF EXISTS `STATS`;
CREATE TABLE `STATS` (
    EMAIL varchar(255) NOT NULL,
    NAME varchar(1024) NOT NULL,
    LEVEL INT NOT NULL,
    DAMAGE INT NOT NULL,
    HEALTH INT NOT NULL,
    ENERGY INT NOT NULL,
    HEALING INT NOT NULL,
    ARMOR INT NOT NULL,
    CRIT INT NOT NULL,
    STUN INT NOT NULL,
    EVADE INT NOT NULL,
    POINTS INT NOT NULL
);