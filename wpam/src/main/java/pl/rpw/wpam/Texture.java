package pl.rpw.wpam;

public class Texture {
    private String email;
    private String texture;

    public void setEmail(String email) {
        this.email = email;
    }

    public void setTexture(String texture) {
        this.texture = texture;
    }

    public String getEmail() {
        return email;
    }

    public String getTexture() {
        return texture;
    }
}
